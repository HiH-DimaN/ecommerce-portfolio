const helpers = {
    formatCurrency(value, locale = 'ru-RU') {
        return new Intl.NumberFormat(locale, {
            style: 'currency',
            currency: 'RUB',
            minimumFractionDigits: 0,
            maximumFractionDigits: 0
        }).format(value);
    },

    formatNumber(value, locale = 'ru-RU') {
        return new Intl.NumberFormat(locale).format(value);
    },

    formatPercent(value) {
        const sign = value > 0 ? '+' : '';
        return `${sign}${value}%`;
    },

    getChangeClass(changeType) {
        return changeType === 'positive' ? 'change-positive' : 'change-negative';
    },

    findQuestion(query) {
        const normalizedQuery = query.toLowerCase().trim();
        
        return data.chatQuestions.find(q => {
            if (q.question.toLowerCase().includes(normalizedQuery)) return true;
            if (q.keywords.some(k => normalizedQuery.includes(k))) return true;
            return false;
        });
    },

    getRandomQuestion() {
        const index = Math.floor(Math.random() * data.chatQuestions.length);
        return data.chatQuestions[index];
    },

    calculateGrowth(before, after) {
        return ((after - before) / before * 100).toFixed(1);
    },

    getTimelineProgress(currentDay) {
        for (const stage of data.timeline) {
            const [start, end] = stage.daysNumeric;
            if (end === null) {
                if (currentDay >= start) return { stage: stage.stage, progress: 100 };
            } else {
                if (currentDay >= start && currentDay <= end) {
                    const progress = ((currentDay - start) / (end - start)) * 100;
                    return { stage: stage.stage, progress: Math.round(progress) };
                }
            }
        }
        return { stage: 1, progress: 0 };
    }
};

